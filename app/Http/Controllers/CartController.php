     /**
     * switchtosaveforlater
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function switchtosaveforlater($id)
    {
        $tovar = Cart::get($id);
        Cart::remove($id);

        Cart::instance('saveForlater')->add($tovar->id, $tovar->name, 1, $tovar->price,1)
        ->associate('App\Tovar');


        return redirect()->route('cart.index');

    }


